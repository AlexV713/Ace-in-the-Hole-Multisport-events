            <h3 class="info-section">What to Bring</h3>

            <p class="hide">Watch the weather closely. The show goes on no matter what the weather is doing.
                <br><br>You must wear your chip timing piece during the entire event. You will be given a band that will
                hold your chip timing
                piece around your ankle throughout the entire event. Be sure that it is snapped tightly. Be sure to have
                your Chip Timing piece on before you start the race and be sure to step over the timing mats after each
                segment of the race. If you are wearing a wet-suit, make sure the timing piece goes under your wetsuit
                otherwise, you will have a very difficult time getting off your wet suit.
                <br><br><strong><span class="titles">Swim:</span></strong><br> Wetsuits are optional for the swim but
                will provide buoyancy and warmth. However, many people opt for no
                wetsuit for a triathlon so no worries. We will provide you with a swim cap but you will want to bring
                your own goggles. <br><br>
                <strong><span class="titles">Bike:</span></strong><br> A biking helmet is mandatory. You will also
                receive 2
                stickers in your package with your race number on them. The small sticker should go on the front of your
                helmet. The bigger sticker will wrap around your bike frame. Road or mountain bikes are acceptable. For
                safety reasons, be sure to have end-caps on the end of your handle-bars.
                <br><br><strong><span class="titles">Run:</span></strong><br> You must finish the race with your bib
                number on the front of you. Some people choose to pin it on at
                the beginning of the race and have it on for the whole event so they don't have to worry about it.
                Others pin it on a singlet that they put on once they finish the swim before they head out for the bike.
                Others use an elastic racing strap that they pin their bib number to and then quickly strap it on before
                they leave for the run. Use whatever option feels best for you.
                <br><br>Remember to bring a change of
                clothing so you can enjoy the post-event festivities.</p>

